			<p class="blog_en">Fu's most beautiful <i>The Mountain Ghost</i> is taken from the ninth poem <i>The Mountain Ghost</i> in the "Nine Songs of Chu", which was described by the famous calligrapher and painter Xiao Ping as "Oriental Venus".<br><br>
			The ninth song of Qu Yuan's "Nine Songs of Chu", <i>The Mountain Ghost</i>, is an ancient Chu sacrificial song for mountain ghosts, which originates from a beautiful and poignant legend. A beautiful and amorous mountain ghost waits eagerly in the mountains for a rendezvous with a gentleman, but the gentleman is late in coming again. The poet uses the inner monologue of the mountain ghost to intertwine fantasy and reality, painting a misty, warm, and sparse atmosphere, creating a magnificent, sincere and smitten image of a god and ghost, which is highly romantic.<br><br>
			In the picture, the mountain ghost, with a long and graceful figure and a cold face, stands on top of a mountain peak under a giant pine with her skirt stretched out and her body as light as the wind; in the distance, the attendant and the red leopard drive the Xingyi carriage in a frenzy of wind and rain, enveloping a mysterious and fantastical atmosphere. The giant pine is painted in a sloppy manner, abandoning the details and seeking only the overall effect.<br><br>
			The figures are light and simple, the dresses and skirts are crisp and sober, while the faces and eyes are delicately drawn, vividly conveying the mood of melancholy anticipation. The painting focuses on ink and washes rendering, using light ink to roll out the dark clouds, and a large brush dipped in ink to swipe the brush rapidly, painting horizontally and vertically in the shape of a storm, highlighting the majesty of the god of life on his journey, while the attendant is hidden between the stormy and thick clouds, with the rain slanting horizontally and the clouds misty and chaotic, creating a mysterious illusionary effect.</p>
			<p class="blog_cn">傅抱石的最美《山鬼》取材于《楚辞·九歌》第九首《山鬼》诗篇，被著名书画家萧平喻为“东方维纳斯”。<br><br>
			屈原《楚辞·九歌》第九首《山鬼》是古代楚人祭祀山鬼的祭歌，源自一个美丽凄婉的传说。一位美丽多情的山鬼，在深山中与公子幽会后，急切等待着，然公子迟迟未能再次到来，惆怅疑惑。诗人采用山鬼内心独白的方式，将幻想与现实交织在一起，摹画了一种烟雨濛濛、温润寥阔的氛围，塑造出一位瑰丽、率真、痴情的神鬼形象，极具浪漫主义色彩。<br><br>
			画面中山鬼身材秀颀，面容冷艳，裙带舒展，身轻如风，立于巨松之下山峰之巅;远处侍从和赤豹驾驭的辛夷车急速前行于风狂雨骤中，笼罩着一种神秘奇幻的气氛。巨松画法粗放，抛弃细部，只求整体效果。<br><br>
			人物造型轻盈简约，衣裙用笔干练洒脱，而面部、眼睛刻画细腻传神，生动传递出惆怅的期待心情。画面注重水墨渲染，以淡墨卷出团团乌云，大笔蘸墨挥笔疾刷，横涂竖抹，呈风雨之状，烘托生命之神出行的威风，而侍从隐没于风雨浓云间，雨丝横斜，云雾迷蒙混沌，营造出一种神秘的幻化效果。</p>
